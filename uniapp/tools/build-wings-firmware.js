#!/usr/bin/env node
// ============================================================
// 翅膀板载资产 v2: 预渲染到 playerScale=27% 后存 base+delta
// 对齐 uniapp terrariaWings.js 渲染逻辑 (挂载点/帧循环)
// ============================================================

const fs = require('fs');
const path = require('path');

const SRC_FILE = path.resolve(__dirname, '../static/terraria/wings.js');
const OUT_FILE = path.resolve(__dirname, '../../esp32-firmware/include/theme_assets/terraria/sprites_wings.h');

const PLAYER_SCALE = 0.27; // 固定缩放
const BODY_FRAME_W = 40;
const BODY_FRAME_H = 56;

// 翅膀挂载点偏移 (对齐 uniapp terrariaWings.js)
// wingCx = playerCenter + (-9) * scale
// wingCy = playerCenter + 9 * scale
// 翅膀 sprite 居中到 (wingCx, wingCy) 后缩放
// 预渲染: 我们只需要缩放后的翅膀 sprite (不含角色位置偏移)
// 存的是翅膀自身缩放后的像素, 板载渲染时再加挂载点偏移

function decodeFrame(wingsData, frameIdx) {
  // wingsData = { w, h, frameCount, fmt, base:{n,b}, deltas:[{n,b}...] }
  const { w, h, fmt, base, deltas } = wingsData;
  const stride = fmt === 7 ? 7 : 5;
  
  function decode(buf, n) {
    const pixels = [];
    for (let i = 0; i < n; i++) {
      const o = i * stride;
      let x, y, r, g, b;
      if (fmt === 7) {
        x = buf[o] | (buf[o+1] << 8); y = buf[o+2] | (buf[o+3] << 8);
        r = buf[o+4]; g = buf[o+5]; b = buf[o+6];
      } else {
        x = buf[o]; y = buf[o+1]; r = buf[o+2]; g = buf[o+3]; b = buf[o+4];
      }
      pixels.push({x, y, r, g, b});
    }
    return pixels;
  }
  
  // base 帧构建完整帧 map
  const baseBuf = Buffer.from(base.b, 'base64');
  const basePixels = decode(baseBuf, base.n);
  const frameMap = new Map();
  for (const p of basePixels) frameMap.set(p.x + ',' + p.y, p);
  
  // 如果是 frame 0 直接返回
  if (frameIdx === 0) return Array.from(frameMap.values());
  
  // 应用 delta
  if (deltas && frameIdx - 1 < deltas.length) {
    const dBuf = Buffer.from(deltas[frameIdx - 1].b, 'base64');
    const dPixels = decode(dBuf, deltas[frameIdx - 1].n);
    for (const p of dPixels) frameMap.set(p.x + ',' + p.y, p);
  }
  return Array.from(frameMap.values());
}

// 缩放翅膀帧到目标尺寸
function scaleFrame(pixels, srcW, srcH, scale) {
  if (pixels.length === 0) return [];
  
  // 建源图 buffer
  const srcBuf = new Uint8Array(srcW * srcH * 4);
  for (const p of pixels) {
    if (p.x >= 0 && p.x < srcW && p.y >= 0 && p.y < srcH) {
      const idx = (p.y * srcW + p.x) * 4;
      srcBuf[idx] = p.r; srcBuf[idx+1] = p.g; srcBuf[idx+2] = p.b; srcBuf[idx+3] = 255;
    }
  }
  
  const dstW = Math.max(1, Math.round(srcW * scale));
  const dstH = Math.max(1, Math.round(srcH * scale));
  const result = [];
  
  for (let dy = 0; dy < dstH; dy++) {
    for (let dx = 0; dx < dstW; dx++) {
      const sx = Math.min(srcW - 1, Math.round(dx / scale));
      const sy = Math.min(srcH - 1, Math.round(dy / scale));
      const idx = (sy * srcW + sx) * 4;
      if (srcBuf[idx+3] > 0) {
        result.push({ x: dx, y: dy, r: srcBuf[idx], g: srcBuf[idx+1], b: srcBuf[idx+2] });
      }
    }
  }
  return result;
}

function computeDelta(basePixels, framePixels) {
  const baseMap = new Map();
  for (const p of basePixels) baseMap.set(p.x + ',' + p.y, (p.r << 16) | (p.g << 8) | p.b);
  const delta = [];
  for (const p of framePixels) {
    const key = p.x + ',' + p.y;
    const baseColor = baseMap.get(key);
    const frameColor = (p.r << 16) | (p.g << 8) | p.b;
    if (baseColor !== frameColor) delta.push(p);
  }
  return delta;
}

function pixelsToBuffer(pixels) {
  const buf = Buffer.alloc(pixels.length * 5);
  for (let i = 0; i < pixels.length; i++) {
    buf[i*5] = pixels[i].x; buf[i*5+1] = pixels[i].y;
    buf[i*5+2] = pixels[i].r; buf[i*5+3] = pixels[i].g; buf[i*5+4] = pixels[i].b;
  }
  return buf;
}

function bufferToCArray(buf) {
  const lines = [];
  for (let i = 0; i < buf.length; i += 16) {
    const chunk = [];
    for (let j = 0; j < 16 && i + j < buf.length; j++) chunk.push('0x' + buf[i+j].toString(16).padStart(2, '0'));
    lines.push('  ' + chunk.join(', ') + (i + 16 < buf.length ? ',' : ''));
  }
  return lines.join('\n');
}

function main() {
  const wingsData = require(SRC_FILE);
  const wingIds = Object.keys(wingsData).map(k => parseInt(k.split('_')[1])).sort((a,b) => a-b);
  
  console.log(`[build-wings v2] ${wingIds.length} wings, scale=${PLAYER_SCALE*100}%`);
  
  let header = `// Generated by build-wings-firmware.js v2
// 42 翅膀预渲染到 playerScale=${PLAYER_SCALE*100}%, base+delta 差异帧
#pragma once
#include <Arduino.h>
#include <pgmspace.h>
#include "terraria_sprite_types.h"

`;

  const entries = [];
  let totalBytes = 0;

  for (const id of wingIds) {
    const key = 'wings_' + id;
    const w = wingsData[key];
    if (!w) continue;
    
    const frameCount = w.frameCount || 4;
    
    // 渲染所有帧并缩放
    const scaledFrames = [];
    for (let f = 0; f < frameCount; f++) {
      const rawFrame = decodeFrame(w, f);
      const scaled = scaleFrame(rawFrame, w.w, w.h, PLAYER_SCALE);
      scaledFrames.push(scaled);
    }
    
    if (scaledFrames[0].length === 0) { console.warn(`  [skip] wings_${id}: 0 px`); continue; }
    
    // 算缩放后 bounding box
    const dstW = Math.max(1, Math.round(w.w * PLAYER_SCALE));
    const dstH = Math.max(1, Math.round(w.h * PLAYER_SCALE));
    
    const varName = `Wings_${id}`;
    
    // base
    const baseBuf = pixelsToBuffer(scaledFrames[0]);
    header += `// wings_${id}: ${dstW}x${dstH}, ${frameCount}f, ${scaledFrames[0].length}px base\n`;
    header += `static const uint8_t k${varName}Base[] PROGMEM = {\n${bufferToCArray(baseBuf)}\n};\n`;
    totalBytes += baseBuf.length;
    
    // deltas
    const deltaNames = [];
    for (let f = 1; f < frameCount; f++) {
      const delta = computeDelta(scaledFrames[0], scaledFrames[f]);
      const dBuf = pixelsToBuffer(delta);
      const dName = `k${varName}D${f-1}`;
      header += dBuf.length > 0 ? `static const uint8_t ${dName}[] PROGMEM = {\n${bufferToCArray(dBuf)}\n};\n` : `static const uint8_t ${dName}[] PROGMEM = {0};\n`;
      deltaNames.push({ name: dName, n: delta.length });
      totalBytes += dBuf.length;
    }
    
    if (deltaNames.length > 0) {
      header += `static const TerrariaFrameBlock k${varName}Deltas[] PROGMEM = {\n`;
      for (const d of deltaNames) header += `  {${d.n}, ${d.name}},\n`;
      header += `};\n`;
      header += `static const TerrariaSpriteAnim k${varName} PROGMEM = {${dstW},${dstH},${frameCount},{${scaledFrames[0].length},k${varName}Base},k${varName}Deltas,5};\n\n`;
    } else {
      header += `static const TerrariaSpriteAnim k${varName} PROGMEM = {${dstW},${dstH},1,{${scaledFrames[0].length},k${varName}Base},nullptr,5};\n\n`;
    }
    
    entries.push({ id, varName, px: scaledFrames[0].length, frames: frameCount });
    console.log(`  wings_${id}: ${dstW}x${dstH}, ${frameCount}f, ${scaledFrames[0].length}+${deltaNames.reduce((s,d)=>s+d.n,0)}px`);
  }
  
  // Getter
  header += `inline const TerrariaSpriteAnim* getWingsScaled(uint16_t id) {\n  switch(id) {\n`;
  for (const e of entries) header += `    case ${e.id}: return &k${e.varName};\n`;
  header += `    default: return nullptr;\n  }\n}\n`;

  fs.writeFileSync(OUT_FILE, header);
  console.log(`\n[done] ${(totalBytes/1024).toFixed(1)} KB binary, ${(fs.statSync(OUT_FILE).size/1024).toFixed(1)} KB .h`);
}

main();
