#!/usr/bin/env node
// ============================================================
// 把 uniapp/static/terraria/sprites_tiles.js + misc.js (forest)
// 转成 esp32-firmware/include/theme_assets/terraria/sprites_biomes.h
//
// 10 biome × 3 tile, 16×16, 用调色板压缩 (≤8 色 → 8bit 索引)
//   每 tile: palette(N×3) + indices(256) ≈ 280 字节
//   10 biome × 3 × 280 ≈ 8 KB (vs 原 fmt=5 的 33 KB 节省 4 倍)
// ============================================================

const fs = require('fs');
const path = require('path');

const TILES_SRC = path.resolve(__dirname, '../static/terraria/sprites_tiles.js');
const MISC_SRC  = path.resolve(__dirname, '../static/terraria/misc.js');
const OUT = path.resolve(__dirname, '../../esp32-firmware/include/theme_assets/terraria/sprites_biomes.h');

// 跟 uniapp BIOME_LIST + _biomeToIndex 严格对齐
const BIOMES = [
  'forest',     // 0
  'corruption', // 1
  'crimson',    // 2
  'jungle',     // 3
  'snow',       // 4
  'dungeon',    // 5
  'underworld', // 6
  'hallow',     // 7
  'ocean',      // 8
  'temple',     // 9
];

function bufferToCArray(buf) {
  const lines = [];
  for (let i = 0; i < buf.length; i += 16) {
    const chunk = [];
    for (let j = 0; j < 16 && i + j < buf.length; j++) {
      chunk.push('0x' + buf[i + j].toString(16).padStart(2, '0'));
    }
    lines.push('  ' + chunk.join(', ') + (i + 16 < buf.length ? ',' : ''));
  }
  return lines.join('\n');
}

// 解码 misc.js 的 forest tile (fmt=5 base64 → pixels 数组)
function decodeMiscTile(miscEntry) {
  const buf = Buffer.from(miscEntry.b, 'base64');
  const w = miscEntry.w, h = miscEntry.h;
  const pixels = [];
  for (let i = 0; i < miscEntry.n; i++) {
    const o = i * 5;
    pixels.push([buf[o], buf[o+1], buf[o+2], buf[o+3], buf[o+4]]);
  }
  return { w, h, pixels };
}

// 直接用 sprites_tiles.js 的 fmt=5 pixels 数组
function getTile(tilesObj, miscObj, biome, idx) {
  if (biome === 'forest') {
    const e = miscObj['biome_forest_' + idx];
    if (!e) throw new Error(`misc.js 缺 biome_forest_${idx}`);
    return decodeMiscTile(e);
  }
  const e = tilesObj[`biome_${biome}_${idx}`];
  if (!e) throw new Error(`sprites_tiles.js 缺 biome_${biome}_${idx}`);
  return e;
}

// 把 16×16 全填充像素 → palette + indices
function paletteEncode(tile) {
  const w = tile.w, h = tile.h;
  const map = new Map();  // "r,g,b" → idx
  const palette = [];
  const indices = new Uint8Array(w * h);
  // 先建立 (x,y) → color 的查找表
  const cmap = new Map();
  for (const p of tile.pixels) cmap.set(p[1] * w + p[0], [p[2], p[3], p[4]]);
  for (let y = 0; y < h; y++) {
    for (let x = 0; x < w; x++) {
      const c = cmap.get(y * w + x);
      if (!c) {
        // 不应该出现; tile 是全填充
        indices[y * w + x] = 0;
        continue;
      }
      const k = `${c[0]},${c[1]},${c[2]}`;
      let idx = map.get(k);
      if (idx === undefined) {
        idx = palette.length;
        map.set(k, idx);
        palette.push(c);
      }
      indices[y * w + x] = idx;
    }
  }
  return { w, h, palette, indices };
}

function main() {
  const tiles = require(TILES_SRC);
  const misc = require(MISC_SRC);

  let header = `// Generated by uniapp/tools/build-biomes-firmware.js
// 10 biome × 3 tile, 16×16 调色板压缩
// 数据格式: palette[N][3] + indices[256]
//   渲染时: indices[y*16+x] → palette[idx][rgb]
#pragma once

#include <Arduino.h>
#include <pgmspace.h>

// 16×16 调色板压缩 tile
struct TerrariaBiomeTile {
  uint8_t palCount;            // 调色板色数
  const uint8_t* palette;      // PROGMEM, palCount * 3 字节 (RGB)
  const uint8_t* indices;      // PROGMEM, 256 字节 (16×16 行优先)
};

`;

  let totalBytes = 0;
  const entries = [];

  for (let bi = 0; bi < BIOMES.length; bi++) {
    const biome = BIOMES[bi];
    for (let ti = 0; ti < 3; ti++) {
      const tile = getTile(tiles, misc, biome, ti);
      if (tile.w !== 16 || tile.h !== 16) {
        throw new Error(`期望 16×16, 实际 ${tile.w}×${tile.h} for ${biome}_${ti}`);
      }
      const enc = paletteEncode(tile);
      const varBase = `BiomeTile_${bi}_${ti}`;

      // palette
      const palBuf = Buffer.alloc(enc.palette.length * 3);
      for (let i = 0; i < enc.palette.length; i++) {
        palBuf[i*3]   = enc.palette[i][0];
        palBuf[i*3+1] = enc.palette[i][1];
        palBuf[i*3+2] = enc.palette[i][2];
      }
      header += `// ${biome}_${ti}: ${enc.palette.length} colors\n`;
      header += `static const uint8_t k${varBase}Pal[] PROGMEM = {\n${bufferToCArray(palBuf)}\n};\n`;

      // indices
      const idxBuf = Buffer.from(enc.indices);
      header += `static const uint8_t k${varBase}Idx[] PROGMEM = {\n${bufferToCArray(idxBuf)}\n};\n`;

      // tile struct
      header += `static const TerrariaBiomeTile k${varBase} PROGMEM = {\n`;
      header += `  .palCount = ${enc.palette.length},\n`;
      header += `  .palette = k${varBase}Pal,\n`;
      header += `  .indices = k${varBase}Idx,\n`;
      header += `};\n\n`;

      totalBytes += palBuf.length + idxBuf.length;
      entries.push({ biome: bi, tile: ti, varBase });
    }
  }

  // getter
  header += `// 取 biome (0..9) 第 idx (0/1/2) 块草地 tile
inline const TerrariaBiomeTile* getBiomeTile(uint8_t biome, uint8_t idx) {
  switch (biome) {
`;
  for (let bi = 0; bi < BIOMES.length; bi++) {
    header += `    case ${bi}:\n      switch (idx) {\n`;
    for (let ti = 0; ti < 3; ti++) {
      header += `        case ${ti}: return &kBiomeTile_${bi}_${ti};\n`;
    }
    header += `        default: return nullptr;\n      }\n`;
  }
  header += `    default: return nullptr;\n  }\n}\n`;

  fs.writeFileSync(OUT, header);
  const fileSize = fs.statSync(OUT).size;
  console.log(`[build-biomes] ${entries.length} tiles, ${totalBytes} bytes pixel data, ${(fileSize/1024).toFixed(1)} KB .h → ${OUT}`);
}

main();
