#!/usr/bin/env node
// ============================================================
// 把 uniapp/static/terraria/*.js (base64 压缩) 转成
//    esp32-firmware/include/theme_assets/terraria/sprites_*.h (PROGMEM 字节数组)
//
// 跑一次产出 8 个 .h 文件 + 1 个 index.h
// ============================================================

const fs = require('fs');
const path = require('path');

const SRC_DIR = path.resolve(__dirname, '../static/terraria');
const OUT_DIR = path.resolve(__dirname, '../../esp32-firmware/include/theme_assets/terraria');

// 8 个 bundle 文件
const BUNDLES = [
  { src: 'armor_heads.js',    out: 'sprites_armor_heads.h',    varPrefix: 'ArmorHead',     keyToId: k => parseInt(k.split('_').pop(), 10) },
  { src: 'armor_bodies.js',   out: 'sprites_armor_bodies.h',   varPrefix: 'ArmorBody',     keyToId: k => parseInt(k.split('_').pop(), 10) },
  { src: 'armor_legs.js',     out: 'sprites_armor_legs.h',     varPrefix: 'ArmorLegs',     keyToId: k => parseInt(k.split('_').pop(), 10) },
  { src: 'wings.js',          out: 'sprites_wings.h',          varPrefix: 'Wings',         keyToId: k => parseInt(k.split('_').pop(), 10), anim: true },
  { src: 'weapons.js',        out: 'sprites_weapons.h',        varPrefix: 'Weapon',        keyToId: k => parseInt(k.split('_').pop(), 10) },
  { src: 'player_layers.js',  out: 'sprites_player_layers.h',  varPrefix: 'PlayerLayer',   keyToId: k => parseInt(k.split('_').pop(), 10) },
  { src: 'summon_guardian.js',out: 'sprites_guardian.h',       varPrefix: 'Guardian',      keyToId: k => k === 'projectile_623' ? 623 : null, anim: true },
  { src: 'misc.js',           out: 'sprites_misc.h',           varPrefix: 'Misc',          keyToId: k => k },
];

function loadBundle(srcFile) {
  const p = path.join(SRC_DIR, srcFile);
  if (!fs.existsSync(p)) throw new Error(`缺失: ${p}`);
  // .js 文件是 module.exports = {...};
  const content = fs.readFileSync(p, 'utf-8');
  const json = content.replace(/^module\.exports\s*=\s*/, '').replace(/;\s*$/, '');
  return JSON.parse(json);
}

function decodeBase64(b64) {
  return Buffer.from(b64, 'base64');
}

// 把 Buffer 输出成 C 字面量数组字符串
//   每行 16 个字节, 用 0x.. 格式
function bufferToCArray(buf) {
  const lines = [];
  for (let i = 0; i < buf.length; i += 16) {
    const chunk = [];
    for (let j = 0; j < 16 && i + j < buf.length; j++) {
      chunk.push('0x' + buf[i + j].toString(16).padStart(2, '0'));
    }
    lines.push('  ' + chunk.join(', ') + (i + 16 < buf.length ? ',' : ''));
  }
  return lines.join('\n');
}

function makeHeader(bundleName) {
  return `// Generated by uniapp/tools/build-firmware-sprites.js
// 不要手改; 改 sprite 请重跑脚本
// Source: uniapp/static/terraria/${bundleName}
#pragma once

#include <Arduino.h>
#include <pgmspace.h>
#include "terraria_sprite_types.h"

`;
}

function emitSingleSprite(varName, sprite) {
  // sprite = { w, h, fmt, n, b: base64 }
  const buf = decodeBase64(sprite.b);
  if (buf.length !== sprite.n * (sprite.fmt === 7 ? 7 : 5)) {
    throw new Error(`Pixel data size mismatch for ${varName}: buf=${buf.length} expected=${sprite.n * (sprite.fmt === 7 ? 7 : 5)}`);
  }
  const arrName = `k${varName}Pixels`;
  let out = '';
  out += `// ${varName}: ${sprite.w}x${sprite.h}, ${sprite.n} pixels, fmt=${sprite.fmt}\n`;
  out += `static const uint8_t ${arrName}[] PROGMEM = {\n`;
  out += bufferToCArray(buf);
  out += `\n};\n`;
  out += `static const TerrariaSprite k${varName} PROGMEM = {\n`;
  out += `  .w = ${sprite.w}, .h = ${sprite.h}, .pixelCount = ${sprite.n},\n`;
  out += `  .pixels = ${arrName}, .fmt = ${sprite.fmt},\n`;
  out += `};\n\n`;
  return out;
}

function emitAnimSprite(varName, anim) {
  // anim = { w, h, frameCount, fmt, base: {n,b}, deltas: [{n,b},...] }
  const stride = anim.fmt === 7 ? 7 : 5;
  let out = '';
  out += `// ${varName}: ${anim.w}x${anim.h}, ${anim.frameCount} frames, fmt=${anim.fmt}\n`;

  // base
  const baseBuf = decodeBase64(anim.base.b);
  const baseArrName = `k${varName}BasePixels`;
  out += `static const uint8_t ${baseArrName}[] PROGMEM = {\n`;
  out += bufferToCArray(baseBuf);
  out += `\n};\n`;

  // 每个 delta
  const deltaArrNames = [];
  for (let i = 0; i < anim.deltas.length; i++) {
    const dBuf = decodeBase64(anim.deltas[i].b);
    const dArrName = `k${varName}Delta${i}Pixels`;
    out += `static const uint8_t ${dArrName}[] PROGMEM = {\n`;
    out += bufferToCArray(dBuf);
    out += `\n};\n`;
    deltaArrNames.push({ name: dArrName, n: anim.deltas[i].n });
  }

  // delta 索引数组
  const deltasArrName = `k${varName}Deltas`;
  out += `static const TerrariaFrameBlock ${deltasArrName}[] PROGMEM = {\n`;
  for (const d of deltaArrNames) {
    out += `  { .pixelCount = ${d.n}, .pixels = ${d.name} },\n`;
  }
  out += `};\n`;

  // wrapper struct
  out += `static const TerrariaSpriteAnim k${varName} PROGMEM = {\n`;
  out += `  .w = ${anim.w}, .h = ${anim.h}, .frameCount = ${anim.frameCount},\n`;
  out += `  .base = { .pixelCount = ${anim.base.n}, .pixels = ${baseArrName} },\n`;
  out += `  .deltas = ${deltasArrName},\n`;
  out += `  .fmt = ${anim.fmt},\n`;
  out += `};\n\n`;

  return out;
}

// 主流程
function main() {
  if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });
  console.log('[build-firmware] 输入:', SRC_DIR);
  console.log('[build-firmware] 输出:', OUT_DIR);
  console.log('');

  // 先输出共享 sprite 类型定义
  const typesH = `// Terraria sprite 类型定义
// 不要手改; 由 uniapp/tools/build-firmware-sprites.js 生成同步
#pragma once

#include <Arduino.h>
#include <pgmspace.h>

// 单帧 sprite
struct TerrariaSprite {
  uint16_t w;
  uint16_t h;
  uint16_t pixelCount;
  const uint8_t* pixels;
  uint8_t fmt;  // 5 = 每像素 5 字节 (x8,y8,r,g,b); 7 = 每像素 7 字节 (x16,y16,r,g,b)
};

// 多帧差异: base (帧 0) + (frameCount-1) 个 delta
struct TerrariaFrameBlock {
  uint16_t pixelCount;
  const uint8_t* pixels;
};

struct TerrariaSpriteAnim {
  uint16_t w;
  uint16_t h;
  uint8_t frameCount;
  TerrariaFrameBlock base;
  const TerrariaFrameBlock* deltas;
  uint8_t fmt;
};
`;
  fs.writeFileSync(path.join(OUT_DIR, 'terraria_sprite_types.h'), typesH);

  let totalBytes = 0;

  // 收集每 bundle 的 sprite 名 (供主索引 emit getXxx switch)
  const indexEntries = {};

  for (const bundle of BUNDLES) {
    console.log(`[${bundle.out}]`);
    const data = loadBundle(bundle.src);
    let out = makeHeader(bundle.src);

    indexEntries[bundle.varPrefix] = [];

    for (const key of Object.keys(data)) {
      // 跳过元字段
      if (key.startsWith('_')) continue;
      const v = data[key];
      if (!v || typeof v !== 'object') continue;

      const id = bundle.keyToId(key);
      if (id == null) {
        console.warn(`  [skip 未知 key]: ${key}`);
        continue;
      }
      const varName = `${bundle.varPrefix}_${id}`;

      if (bundle.anim || (v.base && v.deltas)) {
        out += emitAnimSprite(varName, v);
        indexEntries[bundle.varPrefix].push({ id, varName, anim: true });
      } else {
        out += emitSingleSprite(varName, v);
        indexEntries[bundle.varPrefix].push({ id, varName, anim: false });
      }
    }

    // 同时把元字段 _gridIndex 等导出为常量
    if (data._gridIndex) {
      out += `// 网格位枚举\nenum ${bundle.varPrefix}GridPos : uint8_t {\n`;
      for (const [name, idx] of Object.entries(data._gridIndex)) {
        out += `  ${bundle.varPrefix.toUpperCase()}_GRID_${name.toUpperCase()} = ${idx},\n`;
      }
      out += `};\n\n`;
    }
    if (data._gridLayers) {
      out += `// 网格层 layer 列表\nstatic constexpr uint8_t k${bundle.varPrefix}GridLayers[] = { ${data._gridLayers.join(', ')} };\nstatic constexpr size_t k${bundle.varPrefix}GridLayerCount = ${data._gridLayers.length};\n\n`;
    }

    fs.writeFileSync(path.join(OUT_DIR, bundle.out), out);
    const size = fs.statSync(path.join(OUT_DIR, bundle.out)).size;
    totalBytes += size;
    console.log(`  ${bundle.out}: ${(size / 1024).toFixed(1)} KB`);
  }

  // index.h: 总入口
  let indexH = `// Terraria sprite 主索引
// 不要手改; 由 uniapp/tools/build-firmware-sprites.js 生成
#pragma once

#include "terraria_sprite_types.h"
#include "sprites_armor_heads.h"
#include "sprites_armor_bodies.h"
#include "sprites_armor_legs.h"
#include "sprites_wings.h"
#include "sprites_weapons.h"
#include "sprites_player_layers.h"
#include "sprites_guardian.h"
#include "sprites_misc.h"

namespace TerrariaSprites {

`;

  // 生成 getXxx switch 函数
  function emitGetter(prefix, returnType, deref = '*') {
    const entries = indexEntries[prefix];
    if (!entries || entries.length === 0) return '';

    let s = `inline const ${returnType}* get${prefix}(uint16_t id) {\n  switch (id) {\n`;
    for (const e of entries) {
      s += `    case ${e.id}: return &k${e.varName};\n`;
    }
    s += `    default: return nullptr;\n  }\n}\n\n`;
    return s;
  }
  function emitGetterByString(prefix, returnType) {
    const entries = indexEntries[prefix];
    if (!entries || entries.length === 0) return '';
    let s = `inline const ${returnType}* get${prefix}(const char* name) {\n`;
    for (const e of entries) {
      s += `  if (strcmp(name, "${e.id}") == 0) return &k${e.varName};\n`;
    }
    s += `  return nullptr;\n}\n\n`;
    return s;
  }

  indexH += emitGetter('ArmorHead',   'TerrariaSprite');
  indexH += emitGetter('ArmorBody',   'TerrariaSprite');
  indexH += emitGetter('ArmorLegs',   'TerrariaSprite');
  indexH += emitGetter('Wings',       'TerrariaSpriteAnim');
  indexH += emitGetter('Weapon',      'TerrariaSprite');
  indexH += emitGetter('PlayerLayer', 'TerrariaSprite');
  indexH += emitGetter('Guardian',    'TerrariaSpriteAnim');
  indexH += emitGetterByString('Misc',  'TerrariaSprite');

  // misc 里有 biome_forest_0/1/2, dust_242_f0, extra_171
  // 提供专用 helper
  indexH += `// 便捷查询
inline const TerrariaSprite* getBiomeForest(uint8_t idx) {
  switch (idx) {
    case 0: return getMisc("biome_forest_0");
    case 1: return getMisc("biome_forest_1");
    case 2: return getMisc("biome_forest_2");
    default: return nullptr;
  }
}
inline const TerrariaSprite* getDust242() { return getMisc("dust_242_f0"); }
inline const TerrariaSprite* getExtra171() { return getMisc("extra_171"); }

}  // namespace TerrariaSprites
`;

  fs.writeFileSync(path.join(OUT_DIR, 'index.h'), indexH);
  const indexSize = fs.statSync(path.join(OUT_DIR, 'index.h')).size;
  totalBytes += indexSize;
  console.log(`  index.h: ${(indexSize / 1024).toFixed(1)} KB`);

  console.log('');
  console.log(`[done] 9 个文件 / ${(totalBytes / 1024).toFixed(1)} KB`);
}

main();
